    public CadastrarVenda( Vendedor vendedor) {
        initComponents();
        this.setModal(true);
        Vendedor vendedorL = new Vendedor(vendedor.getIdv(),vendedor.getNome(),vendedor.getLogin(),vendedor.getSenha());
        vendedor_Local = vendedorL;
        
        ProdutoDAOJDBC produtosdao = new ProdutoDAOJDBC();
        List<Produto> produtos;
        produtos = new ArrayList<>();

        try {
            produtos = produtosdao.consultar();
        } catch (Exception ex) {
            Logger.getLogger(JanelaADM.class.getName()).log(Level.SEVERE, null, ex);
        }

        consultarDadosTabelaProdutos(produtos);
        
    }

