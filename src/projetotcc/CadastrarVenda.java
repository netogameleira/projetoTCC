public void consultarDadosTabelaProdutos(List<Produto> produtos) {
       ProdutoTable Tabela_Produtos = new ProdutoTable(produtos);
        jTable_Vendas.setModel(Tabela_Produtos);
        ajustarTamanhoColunasProduto();
    }

    private void ajustarTamanhoColunasProduto() {
        jTable_Vendas.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTable_Vendas.getColumnModel().getColumn(2).setPreferredWidth(100);
    }
    
