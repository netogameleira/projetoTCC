public void consultarDadosTabelaVendas(List<Venda> vendas){    
        VendaTable Tabela_Vendas = new VendaTable(vendas);
        jTable_Vendedor.setModel(Tabela_Vendas);
        ajustarTamanhoColunasVendas();
    }
    private void ajustarTamanhoColunasVendas() {
        jTable_Vendedor.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTable_Vendedor.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTable_Vendedor.getColumnModel().getColumn(2).setPreferredWidth(70);
        jTable_Vendedor.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTable_Vendedor.getColumnModel().getColumn(4).setPreferredWidth(100);
    }

    public void consultarDadosTabelaProdutos(List<Produto> produtos) {
       ProdutoTable Tabela_Produtos = new ProdutoTable(produtos);
        jTable_Vendedor.setModel(Tabela_Produtos);
        ajustarTamanhoColunasProduto();
    }

    private void ajustarTamanhoColunasProduto() {
        jTable_Vendedor.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTable_Vendedor.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTable_Vendedor.getColumnModel().getColumn(2).setPreferredWidth(100);
    }
    
    
